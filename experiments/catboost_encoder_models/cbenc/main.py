"""
main.py
-------
Entry point: parse arguments, resolve the config profile, run the experiment,
write ``report.md`` and print the final research-question report.

Usage
-----
    python -m cbenc.main [--config PATH] [--profile default|full_spec] [--smoke]
"""

from __future__ import annotations

import argparse
from pathlib import Path

from .config import load_config
from .runner import Results, final_report, run

SCRIPT_DIR = Path(__file__).resolve().parent.parent   # experiments/catboost_encoder_models/


def _img(title: str, rel_path: str, caption: str) -> list[str]:
    """Markdown image block, only emitted if the file exists (partial-run safe)."""
    if not (SCRIPT_DIR / rel_path).exists():
        return []
    return [f"![{title}]({rel_path})", "", f"*{caption}*", ""]


def _leaderboard_table(test_df) -> str:
    cols = ["model_name", "average_precision", "roc_auc", "log_loss", "train_time_seconds"]
    sub = test_df.sort_values("average_precision", ascending=False)[cols]
    header = "| " + " | ".join(cols) + " |"
    sep = "|" + "|".join(["---"] * len(cols)) + "|"
    body = []
    for _, r in sub.iterrows():
        cells = [str(r["model_name"])] + [f"{r[c]:.4f}" for c in cols[1:]]
        body.append("| " + " | ".join(cells) + " |")
    return "\n".join([header, sep, *body])


def _write_report(results: Results, report_text: str, out_dir: Path, path: Path) -> None:
    s = results.summary
    test_df = results.metrics[results.metrics.eval_split == "test"]

    lines = [
        "# CatBoost-Encoder Model Comparison — Report",
        "",
        "> Generated by `experiments/catboost_encoder_models/run_experiment.py`.",
        f"> Profile: **{s['profile']}**.",
        "",
        "## Flow",
        "",
        "```",
        "data -> catboost_encoder -> model_i training -> evaluation",
        "```",
        "",
        "`category_encoders.CatBoostEncoder` is an ordered, permutation-based target "
        "encoding that borrows CatBoost's internal \"Ordered TS\" categorical-handling "
        "trick, implemented entirely outside CatBoost as a plain sklearn transformer. "
        "Same data-generating process and undersampling discipline as "
        "`experiments/imbalanced_classification` and `experiments/encoder_comparison`: "
        "train on the 10%-positive `train_under`, evaluate on `val_full`/`test_full` at "
        "the true ~0.1% base rate.",
        "",
        "## Setup",
        "",
        "| Parameter | Value |",
        "|-----------|-------|",
        f"| N_full | {s['full']['n']:,} |",
        f"| pi (true train base rate) | {s['pi_true_train_base_rate']:.5f} |",
        f"| train_under | {s['train_under']['n']:,} (pos rate {s['train_under']['positive_rate']:.3f}) |",
        "| Encoder | CatBoostEncoder (ordered target encoding) |",
        "| Models | logistic, rbf_svm, random_forest, mlp, catboost_encoded (+ catboost_native) |",
        "",
        "## Final report",
        "",
        "```",
        report_text,
        "```",
        "",
        "## Leaderboard (test split)",
        "",
        _leaderboard_table(test_df),
        "",
        "*`catboost_native` has no separate encode step — its categorical split search "
        "happens inside the boosting fit, so its encode timings are n/a.*",
        "",
        "## Plots",
        "",
        "### AP by model",
        "",
        *_img(
            "AP by model", "outputs/plots/ap_by_model/ap_by_model.png",
            "Average precision (test) per model. CatBoost-native is highlighted in red "
            "since it isn't fed the CatBoostEncoder features.",
        ),
        "### Precision-Recall",
        "",
        *_img(
            "Precision-recall", "outputs/plots/precision_recall/precision_recall.png",
            "PR curves (test). CatBoost-native is dashed to distinguish it from the "
            "encoded models.",
        ),
        "See `README.md` for how to interpret the encoder and metrics.",
        "",
    ]
    path.write_text("\n".join(lines) + "\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="CatBoost-encoder model comparison experiment")
    parser.add_argument("--config", default=str(SCRIPT_DIR / "config.yaml"))
    parser.add_argument("--profile", default="default")
    parser.add_argument("--smoke", action="store_true")
    args = parser.parse_args()

    cfg = load_config(args.config, profile=args.profile, smoke=args.smoke)
    out_dir = SCRIPT_DIR / "outputs"
    out_dir.mkdir(parents=True, exist_ok=True)

    results = run(cfg, out_dir)
    report_text = final_report(results.metrics)

    _write_report(results, report_text, out_dir, SCRIPT_DIR / "report.md")

    print("\n" + "=" * 70)
    print(report_text)
    print("=" * 70)


if __name__ == "__main__":
    main()
