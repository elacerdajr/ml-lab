"""
main.py
-------
Entry point: parse arguments, resolve the config profile, run the experiment,
write ``report.md`` and print the final research-question report.

Usage
-----
    python -m enccmp.main [--config PATH] [--profile default|full_spec] [--smoke]
"""

from __future__ import annotations

import argparse
from pathlib import Path

from .config import load_config
from .runner import Results, final_report, run

SCRIPT_DIR = Path(__file__).resolve().parent.parent   # experiments/encoder_comparison/


def _img(title: str, rel_path: str, caption: str) -> list[str]:
    """Markdown image block, only emitted if the file exists (partial-run safe)."""
    if not (SCRIPT_DIR / rel_path).exists():
        return []
    return [f"![{title}]({rel_path})", "", f"*{caption}*", ""]


def _leaderboard_table(test_df) -> str:
    cols = ["model_name", "encoder", "average_precision", "roc_auc", "log_loss",
            "n_features_out", "train_time_seconds"]
    sub = test_df.sort_values("average_precision", ascending=False)[cols]
    header = "| " + " | ".join(cols) + " |"
    sep = "|" + "|".join(["---"] * len(cols)) + "|"
    body = []
    for _, r in sub.iterrows():
        cells = [str(r["model_name"]), str(r["encoder"])] + [
            f"{r[c]:.4f}" if c != "n_features_out" else str(int(r[c])) for c in cols[2:]
        ]
        body.append("| " + " | ".join(cells) + " |")
    return "\n".join([header, sep, *body])


def _diagnostics_table(diag_df) -> str:
    cols = ["encoder", "n_features_out", "fit_time_seconds",
            "val_transform_time_seconds", "test_transform_time_seconds"]
    header = "| " + " | ".join(cols) + " |"
    sep = "|" + "|".join(["---"] * len(cols)) + "|"
    body = []
    for _, r in diag_df.iterrows():
        cells = [str(r["encoder"]), str(int(r["n_features_out"]))]
        for c in cols[2:]:
            v = r[c]
            cells.append("n/a" if v != v else f"{v:.4f}")  # NaN check without pandas import
        body.append("| " + " | ".join(cells) + " |")
    return "\n".join([header, sep, *body])


def _write_report(results: Results, report_text: str, out_dir: Path, path: Path) -> None:
    s = results.summary
    test_df = results.metrics[results.metrics.eval_split == "test"]

    lines = [
        "# Encoder Comparison — Report",
        "",
        "> Generated by `experiments/encoder_comparison/run_experiment.py`.",
        f"> Profile: **{s['profile']}**.",
        "",
        "## Flow",
        "",
        "```",
        "data -> encoder_i -> model_j training -> evaluation",
        "```",
        "",
        "Same data-generating process and undersampling discipline as "
        "`experiments/imbalanced_classification`: train on the 10%-positive "
        "`train_under`, evaluate on `val_full`/`test_full` at the true ~0.1% base rate.",
        "",
        "## Setup",
        "",
        "| Parameter | Value |",
        "|-----------|-------|",
        f"| N_full | {s['full']['n']:,} |",
        f"| pi (true train base rate) | {s['pi_true_train_base_rate']:.5f} |",
        f"| train_under | {s['train_under']['n']:,} (pos rate {s['train_under']['positive_rate']:.3f}) |",
        "| Encoders | onehot, ordinal, frequency, target, hashing (+ CatBoost native) |",
        "| Models | logistic, rff_logistic, mlp, catboost_encoded (+ catboost_native) |",
        "",
        "## Final report",
        "",
        "```",
        report_text,
        "```",
        "",
        "## Leaderboard (test split, all model x encoder combinations)",
        "",
        _leaderboard_table(test_df),
        "",
        "## Encoder diagnostics",
        "",
        "Dimensionality and timing are properties of the encoder alone, independent of "
        "which model consumes its output.",
        "",
        _diagnostics_table(results.diagnostics),
        "",
        "*`native` has no separate encode step — CatBoost's categorical split search "
        "happens inside the boosting fit, so its encode timings are n/a and its "
        "`n_features_out=4` is the raw column count, not a comparable dimensionality.*",
        "",
        "## Plots",
        "",
        "### AP by encoder x model",
        "",
        *_img(
            "AP by encoder", "outputs/plots/ap_by_encoder/ap_by_encoder.png",
            "Grouped bars: average precision (test) for every model x encoder "
            "combination. `catboost_native` appears only at the `native` x-position.",
        ),
        "### AP vs encoded dimensionality",
        "",
        *_img(
            "Dimensionality vs AP", "outputs/plots/dimensionality/dimensionality_vs_ap.png",
            "Does a bigger encoded representation (OneHot) win over compact ones "
            "(Target, Hashing)? `native`'s x=4 is not a fair size comparison — see caveat above.",
        ),
        "### Encode + train time",
        "",
        *_img(
            "Timing", "outputs/plots/timing/timing.png",
            "Stacked bars: encode fit time (lighter) + model train time (darker), per "
            "model x encoder. Is a fancier encoder worth its extra cost?",
        ),
        "See `README.md` for how to interpret each encoder and metric.",
        "",
    ]
    path.write_text("\n".join(lines) + "\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="Encoder comparison experiment")
    parser.add_argument("--config", default=str(SCRIPT_DIR / "config.yaml"))
    parser.add_argument("--profile", default="default")
    parser.add_argument("--smoke", action="store_true")
    args = parser.parse_args()

    cfg = load_config(args.config, profile=args.profile, smoke=args.smoke)
    out_dir = SCRIPT_DIR / "outputs"
    out_dir.mkdir(parents=True, exist_ok=True)

    results = run(cfg, out_dir)
    report_text = final_report(results.metrics)

    _write_report(results, report_text, out_dir, SCRIPT_DIR / "report.md")

    print("\n" + "=" * 70)
    print(report_text)
    print("=" * 70)


if __name__ == "__main__":
    main()
